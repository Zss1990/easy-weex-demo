<template>
  <div class="mzui-demo">
    <scroller class="scroller">
      <title link="https://yanjiie.github.io/weex-flymeui/#/packages/fm-dialog/"></title>
      <category title="确认弹框"></category>
        <div class="container row">
          <fm-button class="btn" @buttonClicked="click1">两个按钮</fm-button>
          <fm-button class="btn" @buttonClicked="click2">多个按钮</fm-button>
          <fm-button class="btn" @buttonClicked="click3">多行弹框</fm-button>
        </div>
      <category title="其他类型"></category>
        <div class="container row">
          <fm-button class="btn" @buttonClicked="click6">弹出单选</fm-button>
          <fm-button class="btn" @buttonClicked="click8">弹出多选</fm-button>
          <fm-button class="btn" @buttonClicked="click7">弹出输入</fm-button>
        </div>

        <fm-dialog :show.sync="checkListShow"
                  title="选择语言"
                  content-type="select"
                  @fmDialogDisappeared="overlayClick"
                  @fmDialogSingleSelected="checkListClick"
                  :can-auto-close="true"
                  :selectData="list"
                  :overlayOpacity="0.1">
        </fm-dialog>

        <fm-dialog :show.sync="checkListShow1"
                  title="选择语言"
                  content-type="select"
                  @fmDialogDisappeared="overlayClick"
                  @fmDialogBtnClicked="checkListClick"
                  :can-auto-close="true"
                  :selectData="list"
                  selectModel="multiple"
                  type="alert"
                  :overlayOpacity="0.1">
        </fm-dialog>

        <fm-dialog :show.sync="inputShow"
                  title="弹出输入"
                  content-type="input"
                  placeholder="提示文本"
                  @fmDialogBtnClicked="inputClick"
                  @fmDialogDisappeared="overlayClick"
                  :can-auto-close="true"
                  :inputDefaultText="inputText">
        </fm-dialog>

        <fm-dialog :show.sync="show"
                  :title="title"
                  :content="content"
                  @fmDialogBtnClicked="btnClick"
                  @fmDialogDisappeared="overlayClick"
                  :can-auto-close="true"
                  :btns="btns">
        </fm-dialog>
    </scroller>
  </div>
</template>

<style scoped>
.mzui-demo {
  position: absolute;
  top: 0;
  right: 0;
  left: 0;
  bottom: 0;
  background-color: #ffffff;
}

.container {
  padding-top: 60px;
  padding-left: 60px;
  padding-right: 60px;
}

.row {
  flex-direction: row;
  flex-wrap: wrap;
}

.btn {
  margin-right: 60px;
}

.scroller {
  position: absolute;
  top: 0;
  right: 0;
  left: 0;
  bottom: 0;
}
</style>

<script>
  import {
    FmButton,
    FmDialog,
    FmCheckboxList,
    FmCheckbox,
    FmInput,
    confirm,
    alert
  } from 'weex-flymeui'
  import Title from '@components/flyme/_mods/title.vue'
  import Category from '@components/flyme/_mods/category.vue'

  const modal = weex.requireModule('modal')

  export default {
    components: {
      Title,
      FmDialog,
      Category,
      FmButton,
      FmCheckboxList,
      FmCheckbox,
      FmInput
    },
    data: () => ({
      show: false,
      checkListShow: false,
      checkListShow1: false,
      inputShow: false,
      checkList: [],
      inputText: '',
      title: '',
      content: '',
      btns: [],
      list: [
        {
          title: '简体中文'
        },
        {
          title: '英文'
        },
        {
          title: '西班牙语'
        }
      ]
    }),
    methods: {
      click1 () {
        this.title = '两个按钮的弹框'
        this.content =
          'fm-dialog 默认两个按钮，按钮文字可通过 cancelText 以及 confirmText 修改'
        this.btns = []
        this.show = true
      },
      click2 () {
        this.title = '多个按钮的弹框'
        this.content =
          '按钮超过 2 个会变成竖排按钮，需要一行多个按钮可通过 slot="btn-group" 进行插入'
        this.btns = [
          {
            text: '按钮1',
            color: 'red'
          },
          {
            text: '按钮2'
          },
          {
            text: '按钮3'
          },
          {
            text: '按钮4'
          },
          {
            text: '按钮5'
          }
        ]
        this.show = true
      },
      click3 () {
        this.title =
          '这是多行标题的弹框通过 props-title 或者通过 slot="title" 插入这是多行标题的弹框通过 props-title 或者通过 slot="title" 插入'
        this.content = ''
        this.btns = []
        this.show = true
      },
      click4 () {
        confirm({
          title: '这是 js 调用弹出的弹框',
          message:
            '用户操作后返回的是一个 Promise 对象，可自行做处理。弹框的取消以及确定字体可以通过 cancelText 以及 confirmText 属性进行更改'
        }).then(
          () => {
            modal.toast({ message: '确定' })
          },
          () => {
            modal.toast({ message: '取消' })
          }
        )
      },
      click5 () {
        alert({
          title: '这是 js 调用弹出的提示框',
          message:
            '用户操作后返回的是一个 Promise 对象，可自行做处理。弹框的确定按钮可以通过 confirmText 属性进行更改'
        }).then(
          () => {
            modal.toast({ message: '提示框消失了' })
          },
          () => {
            modal.toast({ message: '点击蒙层消失了' })
          }
        )
      },
      click6 () {
        this.checkListShow = true
        this.checkList = []
      },
      click7 () {
        this.inputShow = true
      },
      click8 () {
        this.checkListShow1 = true
      },
      btnClick (btn) {
        if (btn.type === 'cancel') {
          modal.toast({ message: '取消' })
        } else if (btn.type === 'confirm') {
          modal.toast({ message: '确定' })
        } else {
          modal.toast({ message: btn.text })
        }
      // console.log(this.show)
      // this.show = false;
      },
      overlayClick () {
        // this.show = false;
        // this.inputShow = false;
        // this.checkListShow = false;
        // this.checkListShow1 = false;
      },
      checkListClick (e) {
        modal.toast({
          message: '选择了: ' + e.selectList.map(item => item.title).toString()
        })
      // this.checkListShow = false;
      // this.checkListShow1 = false;
      },
      checkListOverlayClick () {
        // this.checkListShow = true;
        // this.checkListShow1 = true;
      },
      inputClick (e) {
        if (e.type === 'cancel') {
          modal.toast({ message: '取消' })
        } else if (e.type === 'confirm') {
          modal.toast({ message: '输入的是: ' + e.inputValue })
        } else {
          modal.toast({ message: e.text })
        }
      // this.inputShow = false;
      },
      inputing (e) {
        this.inputText = e.value
      }
    }
  }
</script>
